import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {

    public static void main(String[] args) {
        Scanner s = new Scanner(System.in);
        int line = 1;
        while (s.hasNext()) {
            System.out.println(line + " " + s.nextLine());
            line++;
        }

    }
}